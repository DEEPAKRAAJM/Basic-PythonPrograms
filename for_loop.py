###### Python Code explaining different for loops' operation ########

primes= [1,2,3,4,5,7,8,9,0]
for prime in primes: # General Declaration for full access
	print(prime)
for x in range(5): # Setting a range (i.e Max value)
	print (x)
for x in range(5,10): # Setting between range
	print (x)
for x in range(5,10,2): # Setting between the range 5-10 and with the difference of 2
	print (x)

